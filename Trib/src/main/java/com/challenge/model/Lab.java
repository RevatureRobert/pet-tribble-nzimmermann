package com.challenge.model;

public class Lab {

    private int id;
    private int tribbleId;
    private int tribbleOccupancy;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getTribbleId() {
        return tribbleId;
    }

    public void setTribbleId(int tribbleId) {
        this.tribbleId = tribbleId;
    }

    public int getTribbleOccupancy() {
        return tribbleOccupancy;
    }

    public void setTribbleOccupancy(int tribbleOccupancy) {
        this.tribbleOccupancy = tribbleOccupancy;
    }
}
